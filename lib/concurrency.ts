/**
 * Runs `fn` over `items` with at most `limit` in flight; preserves input order
 * in the result array. Shared by the pipeline's score loop and the GitHub read
 * layer's head-SHA resolution so neither serializes I/O it could overlap.
 */
export async function mapWithConcurrency<T, R>(
  items: T[],
  limit: number,
  fn: (item: T, index: number) => Promise<R>,
): Promise<R[]> {
  const results = new Array<R>(items.length);
  let next = 0;
  async function worker() {
    while (next < items.length) {
      const i = next++;
      results[i] = await fn(items[i], i);
    }
  }
  await Promise.all(
    Array.from({ length: Math.min(limit, items.length) }, worker),
  );
  return results;
}
